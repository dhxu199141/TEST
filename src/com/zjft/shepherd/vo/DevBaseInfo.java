package com.zjft.shepherd.vo;

public class DevBaseInfo {
	private String devNo;
	private String ip;
	private Integer packetLen;
	private Integer vizpType;
	private String serial;
	public String getDevNo() {
		return devNo;
	}
	public void setDevNo(String devNo) {
		this.devNo = devNo;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public Integer getPacketLen() {
		return packetLen;
	}
	public void setPacketLen(Integer packetLen) {
		this.packetLen = packetLen;
	}
	public Integer getVizpType() {
		return vizpType;
	}
	public void setVizpType(Integer vizpType) {
		this.vizpType = vizpType;
	}
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
	
}
