package com.zjft.shepherd.vo;

public class ChkDevInfo
{
	private String devNo = null;
	private String serialNo = null;
	private String ip = null;
	private String orgNo = null;
	private String devType = null;
	private String devVendor = null;
	private String devCatalog = null;
	private String atmcSoft = null;
	private String installType = null;
	
	public String getDevNo() {
		return devNo;
	}
	public void setDevNo(String devNo) {
		this.devNo = devNo;
	}
	public String getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getOrgNo() {
		return orgNo;
	}
	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}
	public String getDevType() {
		return devType;
	}
	public void setDevType(String devType) {
		this.devType = devType;
	}
	public String getDevVendor() {
		return devVendor;
	}
	public void setDevVendor(String devVendor) {
		this.devVendor = devVendor;
	}
	public String getDevCatalog() {
		return devCatalog;
	}
	public void setDevCatalog(String devCatalog) {
		this.devCatalog = devCatalog;
	}
	public String getAtmcSoft() {
		return atmcSoft;
	}
	public void setAtmcSoft(String atmcSoft) {
		this.atmcSoft = atmcSoft;
	}
	public String getInstallType() {
		return installType;
	}
	public void setInstallType(String installType) {
		this.installType = installType;
	}
}
